<template>
  <div>
    <v-footer :color="componentColor" height="80px">
      <v-row v-if="vehicle && vehicle.id" justify="center">
        <v-col class="agency">
          <span class="d-none d-md-block">{{ agency.name }}</span>
          <span class="d-md-none">{{ agency.shortName }}</span>
        </v-col>
        <v-col class="ref">
          {{ vehicle.label || vehicle.ref }}
        </v-col>
        <v-col v-if="vehicle.bearing" class="bearing d-none d-md-block">
          <v-icon :style="{ transform: 'rotate(' + vehicle.bearing + 'deg)' }">
            {{ mdiSvg.navigation }}
          </v-icon>
        </v-col>
        <v-col v-if="vehicle.speed" class="speed d-none d-md-block">
          {{ vehicle.speed }} km/h
        </v-col>
        <v-col class="trip">
          <div class="route" :style="tripStyle">
            {{ vehicle.trip.routeShortName || vehicle.routeId }}
            <span class="d-none d-md-block">
              {{ vehicle.trip.routeLongName }}
            </span>
          </div>
        </v-col>
        <v-col class="expand">
          <v-btn text icon @click="$emit('open-sheet')">
            <v-icon>
              {{ mdiSvg.chevronUp }}
            </v-icon>
          </v-btn>
        </v-col>
      </v-row>
      <v-row v-else align="center" justify="center">
        <v-col class="select">
          {{ $t('mapFooter.select') }}
        </v-col>
      </v-row>
    </v-footer>
  </div>
</template>

<script>
import { mdiNavigation, mdiChevronUp } from '@mdi/js'
export default {
  filters: {
    uppercase: (value) => {
      return value.toUpperCase()
    },
  },
  data: () => ({
    mdiSvg: {
      navigation: mdiNavigation,
      chevronUp: mdiChevronUp,
    },
  }),
  computed: {
    agency() {
      return this.$store.state.agencies.data[this.vehicle?.agency]
    },
    vehicle() {
      return this.$store.state.vehicles.selection
    },
    componentColor() {
      return this.$vuetify.theme.dark ? '' : 'grey lighten-4'
    },
    tripStyle() {
      return {
        backgroundColor: this.colorFilter(this.vehicle.trip.routeColor),
        color: this.colorFilter(this.vehicle.trip.routeTextColor),
        borderColor:
          this.colorFilter(this.vehicle.trip.routeColor) || this.agency.color,
      }
    },
  },
  methods: {
    colorFilter(value) {
      if (typeof value !== 'string') return null
      if (value.startsWith('#')) return value
      return '#'.concat(value)
    },
  },
}
</script>

